def add_numbers(a: int, b: int) -> int:
    """
    Adds two numbers and returns the result.

    Parameters:
    a (int): The first number to add.
    b (int): The second number to add.

    Returns:
    int: The sum of the two numbers.

    Example:
    >>> add_numbers(3, 5)
    8
    """
    return a + b